var color = ["yellow", "pink", "black", "red", "blue"];

console.log(color[1]);

//push add data to array
console.log("Method Push");
color.push("green");
console.log(color);

// pop remove data start index
console.log("Method pop");
color.pop();
console.log(color);

// pop remove data start index
console.log("Method shift");
color.shift();
console.log(color);

// splie remove data in array
console.log("Method splie");
color.splice("2,1");
console.log(color);
